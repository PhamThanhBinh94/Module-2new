public class KhaiBaoBien {
    public static void main(String[] args) {
        int i=10;
        float f= 20.5f;
        double d= 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("i là: " +i);
        System.out.println("f là: " +f);
        System.out.println("d là: " +d);
        System.out.println("b là: " +b);
        System.out.println("c là: " +c);
        System.out.println("s là: " +s);
    }
}
